def get_category(num):
    e = int(num)
    if 100 <= e <= 199: return "Оцветител"
    elif 200 <= e <= 299: return "Консервант"
    elif 300 <= e <= 399: return "Антиоксидант"
    elif 400 <= e <= 499: return "Сгъстител / стабилизатор"
    elif 500 <= e <= 599: return "Регулатор на киселинност"
    elif 600 <= e <= 699: return "Подобрител на вкус"
    elif 900 <= e <= 999: return "Подсладител / газ / глазиращ агент"
    elif 1000 <= e <= 1520: return "Други добавки"
    else: return "Неизвестно"
 
# -------------------------------------------------------------------
# OCR с Claude API - чете снимката и връща текста
# -------------------------------------------------------------------
def read_text_with_claude(img, api_key):
    # конвертираме снимката в base64
    img_rgb = img.convert("RGB")
    buffer = BytesIO()
    img_rgb.save(buffer, format="JPEG", quality=90)
    img_base64 = base64.b64encode(buffer.getvalue()).decode("utf-8")
 
    client = anthropic.Anthropic(api_key=api_key)
 
    response = client.messages.create(
        model="claude-opus-4-5",
        max_tokens=1000,
        messages=[
            {
                "role": "user",
                "content": [
                    {
                        "type": "image",
                        "source": {
                            "type": "base64",
                            "media_type": "image/jpeg",
                            "data": img_base64,
                        },
                    },
                    {
                        "type": "text",
                        "text": "Прочети ЦЕЛИЯ текст от тази снимка на хранителен етикет. Върни само текста, без обяснения. Запази всички Е-номера (като E450, Е621 и т.н.) точно както са написани."
                    }
                ],
            }
        ],
    )
 
    return response.content[0].text
 
# -------------------------------------------------------------------
# ТЪРСЕНЕ НА Е-ТА
# -------------------------------------------------------------------
def find_e_numbers(text):
    # поправяме OCR грешки
    text = text.replace('[', 'E').replace('|', 'E')
    text = text.replace('€', 'E').replace('{', 'E')
    # кирилско Е -> латинско E
    text = text.replace('Е', 'E').replace('е', 'e')
 
    pattern = r"e[\s\-_]?\d{3,4}"
    results = re.findall(pattern, text.lower())
 
    cleaned = []
    for r in results:
        r = re.sub(r'[\s\-_]', '', r)
        if r not in cleaned:
            cleaned.append(r)
    return cleaned
 
