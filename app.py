# ПОДОБРЯВАНЕ НА СНИМКАТА преди OCR - това помага много!
# -------------------------------------------------------------------
def preprocess_image(img):
    img = img.convert("RGB")
 
    # ако е малка я уголемяваме - easyocr чете по-добре на по-голями снимки
    w, h = img.size
    if w < 1000:
        scale = 1000 / w
        img = img.resize((int(w * scale), int(h * scale)), Image.LANCZOS)
 
    # повишаваме контраста за по-ясен текст
    img = ImageEnhance.Contrast(img).enhance(2.0)
 
    # повишаваме рязкостта
    img = ImageEnhance.Sharpness(img).enhance(2.5)
 
    # малко по-светло
    img = ImageEnhance.Brightness(img).enhance(1.2)
 
    return img
 
# -------------------------------------------------------------------
# ТЪРСЕНЕ НА Е-ТА - оправен за OCR грешки
# -------------------------------------------------------------------
def find_e_numbers(text):
    # OCR много често бърка тези символи с Е
    text = text.replace('[', 'E').replace('|', 'E').replace('€', 'E')
 
    # кирилско Е -> латинско E
    text = text.replace('Е', 'E').replace('е', 'e')
 
    text_lower = text.lower()
 
    # търсим: e621, e 621, e-621
    pattern = r"e[\s\-]?\d{3,4}"
    results = re.findall(pattern, text_lower)
 
    # почистваме - махаме интервали и тирета
    cleaned = []
    for r in results:
        r = re.sub(r'[\s\-]', '', r)
        if r not in cleaned:
            cleaned.append(r)
 
    return cleaned
 
